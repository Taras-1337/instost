struct d_variant
{
    union
    {
        float    m_Float;
        long     m_Int;
        char    *m_pString;
        void    *m_pData;
        float    m_Vector[3];
        int64_t  m_Int64;
    };

    int m_Type;
};

struct crecv_proxy_data
{
    const struct recv_prop *m_pRecvProp;
    struct d_variant        m_Value;
    int                     m_iElement;
    int                     m_ObjectID;
};

typedef void (*recv_var_proxy_fn) (const struct crecv_proxy_data *pData,
                                   void *pStruct,
                                   void *pOut);
typedef void *(*create_client_class_fn)(int entnum, int serial_num);

struct recv_prop
{
    char              *m_pVarName;
    int                m_RecvType;
    int                m_Flags;
    int                m_StringBufferSize;
    bool               m_bInsideArray;
    const void        *m_pExtraData;
    struct recv_prop  *m_pArrayProp;
    void              *m_ArrayLengthProxy;
    recv_var_proxy_fn  m_ProxyFn;
    void              *m_DataTableProxyFn;
    struct recv_table *m_pDataTable;
    int                m_Offset;
    int                m_ElementStride;
    int                m_nElements;
    const char        *m_pParentArrayPropName;
};

struct recv_table
{
    struct recv_prop *m_pProps;
    int               m_nProps;
    void             *m_pDecoder;
    char             *m_pNetTableName;
    bool              m_bInitialized;
    bool              m_bInMainList;
};

struct client_class
{
    create_client_class_fn  m_pCreateFn;
    void                   *m_pCreateEventFn;
    char                   *m_pNetworkName;
    struct recv_table      *m_pRecvTable;
    struct client_class    *m_pNext;
    enum class_ids          m_ClassID;
};

struct client_class *
get_all_classes(void *self);
